namespace AMS_OOP_Heranca
{
    class Vehicle
    {
         public String id ="jus";
         public String description="marca";

        public Vehicle(String id, String description)
        {
            this.id = id;
            this.description = description;

        }
        public String getId()
        {
            return id;
        }
        public String getDes()
        {
            return description;
        }




    }


}